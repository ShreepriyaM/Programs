// Write a C++ program to print squares of numbers between 1 and 25

#include <iostream>

using namespace std;

int main() {
    for (int i = 1; i <= 25; i++) {
        int square = i * i;
        cout << "Square of " << i << ": " << square << endl;
    }

    return 0;
}

